def pick_story(seed: int) -> dict:
    """按 seed 挑一本。同一个 seed 永远挑到同一本（调用方用 day + couple_id）。"""
    return LOCAL_STORIES[seed % len(LOCAL_STORIES)]


def local_round(story: dict, round_no: int) -> dict:
    """第 round_no 幕（从 1 开始）。越界返回结局幕（无选项）。"""
    rounds = story["rounds"]
    if 1 <= round_no <= len(rounds):
        r = rounds[round_no - 1]
        return {"scene": r["scene"], "options": list(r["options"])}
    return {"scene": story["ending"], "options": []}


def local_ending(story: dict) -> str:
    return story["ending"]
