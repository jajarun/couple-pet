"""纯函数：每日一问的本地题库 + 选题。无 DB / 时钟 / AI。混味=三种 flavor 轮换。"""

FLAVORS = ["ambiguous", "deep", "silly"]

LOCAL_QUESTIONS = {
    # 暧昧撩拨
    "ambiguous": [
        "我身上你最想咬一口的地方是哪儿?",
        "如果现在能瞬移到我身边,你第一件事想干嘛?",
        "我做过最让你心动的一个小动作是什么?",
        "今晚想在梦里跟我做点啥?",
        "我哪个表情或角度最让你把持不住?",
        "如果只能亲我一个地方,你选哪儿?",
        "有件想跟我一起做的事,你偷偷想过但还没说的?",
        "我说哪句话会让你耳朵发烫?",
        "现在最想被我怎么撩?",
        "我穿成什么样,你会看到走不动道?",
    ],
    # 深度了解
    "deep": [
        "这周末完全归你安排,你想我们怎么过?",
        "你最近有什么没说出口的小情绪?",
        "我做的哪件事,让你觉得'被偏爱'了?",
        "你理想中五年后的我们,在过什么样的日子?",
        "最近哪一刻,你突然很想我?",
        "你最希望我更懂你哪一点?",
        "有没有一件小事,我一直没做、但你很想我做?",
        "你觉得我们之间最珍贵的默契是什么?",
        "上次我惹你不开心,你当时心里其实在想什么?",
        "什么样的瞬间,会让你觉得'还好有你'?",
    ],
    # 沙雕无厘头
    "silly": [
        "如果我变成一只狗,你会给我起什么名?",
        "给我们的爱情起个菜名,你选啥?",
        "如果只能用一种动物形容我,是啥?",
        "我的睡姿像哪种生物?",
        "世界末日,只能带我或者充电宝,你诚实选谁?",
        "给我编一个三个字的沙雕外号。",
        "我俩合体变成一道菜,叫什么名字?",
        "我最近做过最蠢、但你觉得最可爱的事是啥?",
        "用一个 emoji 总结今天的我。",
        "如果给我们的关系拍部电影,片名叫啥?",
    ],
}


def choose_flavor(seed: int) -> str:
    return FLAVORS[seed % len(FLAVORS)]


def pick_local(flavor: str, exclude: set[str], seed: int) -> str:
    bank = LOCAL_QUESTIONS[flavor]
    pool = [q for q in bank if q not in exclude] or bank
    return pool[seed % len(pool)]
