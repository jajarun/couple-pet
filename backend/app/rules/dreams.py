def _pick(pool: dict, branch: str, seed: int) -> str:
    lines = pool.get(branch) or pool[BALANCED]
    return lines[seed % len(lines)]


def pick_dream(branch: str, seed: int) -> str:
    return _pick(LOCAL_DREAMS, branch, seed)


def pick_runaway_note(branch: str, seed: int) -> str:
    return _pick(LOCAL_RUNAWAY_NOTES, branch, seed)
